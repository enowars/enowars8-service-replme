type CRCUtil struct {
	Degree   uint
	DegreeBI *big.Int
	Table    [256]*big.Int
	Mask     *big.Int
}

func CRC() CRCUtil {

	var table [256]*big.Int

	for i, v := range CRC_TABLE {
		num, _ := new(big.Int).SetString(v, 16)

		if num == nil {
			panic("Failed to create table")
		}

		table[i] = num
	}

	degree := big.NewInt(CRC_DEGREE)

	mask := new(big.Int).Sub(
		new(big.Int).Exp(
			big.NewInt(2),
			degree,
			nil,
		),
		big.NewInt(1),
	)

	return CRCUtil{
		Degree:   uint(CRC_DEGREE),
		DegreeBI: degree,
		Table:    table,
		Mask:     mask,
	}
}

func (crc *CRCUtil) Calculate(s []byte) *big.Int {
	result := big.NewInt(0)
	shift := crc.Degree - 8
	byteMask := big.NewInt(0xFF)

	for _, b := range s {
		idx := new(big.Int).And(
			new(big.Int).Xor(
				new(big.Int).Rsh(
					result,
					shift,
				),
				big.NewInt(int64(b)),
			),
			byteMask,
		).Uint64()

		result = new(big.Int).And(
			new(big.Int).Xor(
				crc.Table[idx],
				new(big.Int).Lsh(
					result,
					8,
				),
			),
			crc.Mask,
		)
	}

	return result
}

